export class AddressValidator {
    static isValidIPv4(ip: string): boolean {
        const ipv4Pattern = /^(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/;
        return ipv4Pattern.test(ip);
    }
    
    static isValidIPv4WithCIDR(ip: string): boolean {
        const ipv4CIDRPattern = /^(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\/(3[0-2]|[12]?[0-9])$/;
        return ipv4CIDRPattern.test(ip);
    }
    
    static isValidIPv4OrCIDR(ip: string): boolean {
        return AddressValidator.isValidIPv4(ip) || AddressValidator.isValidIPv4WithCIDR(ip);
    }
}